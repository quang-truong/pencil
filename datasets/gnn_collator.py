"""
GNN-specific collator for batching PyTorch Geometric Data objects from ShaDowKHopSeqFromEdgesMapDataset.

This collator handles the batching of subgraphs generated by k-hop sampling for link prediction tasks.
It properly combines multiple Data objects into a single batch while preserving the structure
needed for GNN training.
"""

from typing import List, Optional
import torch
from torch_geometric.data import Batch


class GNNCollator:
    """
    Collator for batching PyTorch Geometric Data objects from ShaDowKHopSeqFromEdgesMapDataset.

    This collator takes a list of (idx, data) tuples and creates a batched Data object
    suitable for GNN training. It handles:
    - Combining multiple subgraphs into a single batch
    - Properly relabeling node indices across subgraphs
    - Preserving edge attributes and node features
    - Maintaining target edge information for link prediction
    """

    def __init__(self, follow_batch: Optional[List[str]] = None):
        """
        Initialize the GNN collator.

        Args:
            follow_batch: List of attribute names to track batch assignments for.
                         Defaults to ['root_n_index', 'seed_node'] for link prediction tasks.
        """
        if follow_batch is None:
            follow_batch = ["root_n_index", "seed_node"]
        self.follow_batch = follow_batch

    def __call__(self, batch_list: List[tuple]) -> tuple:
        """
        Collate a batch of (idx, data) tuples into a single batched Data object.

        Args:
            batch_list: List of (idx, data) tuples where:
                - idx: Sample index (int)
                - data: PyTorch Geometric Data object containing subgraph information

        Returns:
            tuple: (batch_indices, batched_data) where:
                - batch_indices: Tensor of original sample indices
                - batched_data: Batched Data object containing all subgraphs
        """
        if not batch_list:
            raise ValueError("Empty batch list provided")

        # Separate indices and data objects
        batch_indices = []
        data_objects = []

        for idx, data in batch_list:
            batch_indices.append(idx)
            # Rename root_n_id to root_n_index for proper offsetting during batching
            if hasattr(data, "root_n_id"):
                data.root_n_index = data.root_n_id.clone()
            data_objects.append(data)

        # Convert indices to tensor
        batch_indices = torch.tensor(batch_indices, dtype=torch.long)

        # Use PyTorch Geometric's Batch.from_data_list for efficient batching
        # This handles node relabeling, edge index concatenation, and feature stacking
        batched_data = Batch.from_data_list(
            data_objects, follow_batch=self.follow_batch
        )

        # Reshape root_n_index to 2D [batch_size, 2] for MPNN model compatibility
        # PyG batches 1D tensors into a 1D tensor, but MPNN expects 2D
        if (
            hasattr(batched_data, "root_n_index")
            and batched_data.root_n_index.dim() == 1
        ):
            batch_size = len(batch_list)
            batched_data.root_n_index = batched_data.root_n_index.view(batch_size, 2)

        # Add batch indices as an attribute for tracking
        batched_data.batch_indices = batch_indices

        return batch_indices, batched_data
